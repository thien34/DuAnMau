    void loadModel(List<Learner> list) {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        list.forEach(o -> {
            model.addRow(new Object[]{
                o.getId(),
                o.getName(),
                o.getGender() ? "Nam" : "Nữ",
                o.getBirth(),
                o.getPhone(),
                o.getEmail(),
                o.getIdEmployee(),
                o.getRegistration()
            });
        });
    }

    void setModel(Learner model) {
        jTextField1.setText(model.getId());
        jTextField2.setText(model.getName());
        jComboBox1.setSelectedItem(model.getGender() != null ? model.getGender() ? "Nam" : "Nữ" : "Nam");
        jTextField3.setText(model.getBirth());
        jTextField4.setText(model.getPhone());
        jTextField5.setText(model.getEmail());
        jTextArea1.setText(model.getNote());
    }

    Learner getModel() {
        return Learner.builder()
                .id(jTextField1.getText())
                .idEmployee(AuthHelper.USER.getId())
                .name(jTextField2.getText())
                .gender(jComboBox1.getSelectedItem().equals("Nam"))
                .birth(jTextField3.getText())
                .phone(jTextField4.getText())
                .email(jTextField5.getText())
                .note(jTextArea1.getText())
                .registration(LocalDate.now().toString())
                .build();
    }

    void setStatus(boolean insertable) {
        jTextField1.setEditable(insertable);
        jButton1.setEnabled(insertable);
        jButton2.setEnabled(!insertable);
        jButton3.setEnabled(!insertable);
        boolean first = this.index > 0;
        boolean last = this.index < jTable1.getRowCount() - 1;
        jButton5.setEnabled(!insertable && first);
        jButton6.setEnabled(!insertable && first);
        jButton7.setEnabled(!insertable && last);
        jButton8.setEnabled(!insertable && last);
    }

