    void loadComYear() {
        DefaultComboBoxModel model2 = (DefaultComboBoxModel) jComboBox2.getModel();
        model2.removeAllElements();
        List<Integer> list = cs.getYears();
        list.forEach(o -> {
            model2.addElement(o);
        });
    }

